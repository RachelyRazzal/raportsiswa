            else jkel = "Perempuan";
            stat.setString(6, jkel);
            
            stat.setString(7, cpendidikan.getSelectedItem().toString());
            stat.setString(8, talamatmurid.getText());
            stat.setString(9, tekstra.getText());
            stat.setString(10, thobi.getText());
            stat.setString(11, tnamaayah.getText());
            stat.setString(12, tpekerjaanayah.getText());
            stat.setString(13, tnamaibu.getText());
            stat.setString(14, tpekerjaanibu.getText());
            stat.setString(15, tno.getText());
            stat.setString(16, talamatortu.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            tnoinduk.requestFocus();
            datatable();
