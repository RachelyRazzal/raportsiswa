    protected void datatable() {
        Object [] baris = {"No Induk Siswa","Nama Peserta Didik","NISN","Tempat,Tanggal Lahir","Agama","Jenis Kelamin","Pendidikan Sebelumnya","Alamat Peserta Didik","Ekstrakulikuler","Hobi","Nama Ayah","Pekerjaan Ayah","Nama Ibu","Pekerjaan Ibu","No Telp/Hp","Alamat Orang Tua"};
        tabmode = new DefaultTableModel(null, baris);
        tabelmurid.setModel(tabmode);
        String sql = "select * from murid";
        
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("noinduk");
                String b = hasil.getString("nama");
                String c = hasil.getString("nisn");
                String d = hasil.getString("ttl");
                String e = hasil.getString("agama");
                String f = hasil.getString("jk");
                String g = hasil.getString("pendidikan");
                String h = hasil.getString("alamatmurid");
                String i = hasil.getString("ekstrakulikuler");
                String j = hasil.getString("hobi");
                String k = hasil.getString("namaayah");
                String l = hasil.getString("pekerjaanayah");
                String m = hasil.getString("namaibu");
                String n = hasil.getString("pekerjaanibu");
                String o = hasil.getString("nohportu");
                String p = hasil.getString("alamatortu");
                String [] data = {a,b,c,d,e,f,g,h,i,j,k,l,m,n,o,p};
                tabmode.addRow(data);  
            }
        } catch (Exception e) {}
    }

